package org.xcolab.jspTags.discussion.exceptions;

public class DiscussionsException extends Exception {

    public DiscussionsException() {
        super();
    }

    public DiscussionsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public DiscussionsException(String message, Throwable cause) {
        super(message, cause);
    }

    public DiscussionsException(String message) {
        super(message);
    }

    public DiscussionsException(Throwable cause) {
        super(cause);
    }

}
