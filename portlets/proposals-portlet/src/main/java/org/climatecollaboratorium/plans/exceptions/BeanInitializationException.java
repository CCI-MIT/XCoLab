package org.climatecollaboratorium.plans.exceptions;

public class BeanInitializationException extends Exception {

    public BeanInitializationException() {
        super();
    }

    public BeanInitializationException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public BeanInitializationException(String arg0) {
        super(arg0);
    }

    public BeanInitializationException(Throwable arg0) {
        super(arg0);
    }

}
