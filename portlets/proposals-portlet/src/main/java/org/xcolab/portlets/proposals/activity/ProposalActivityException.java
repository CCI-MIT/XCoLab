package org.xcolab.portlets.proposals.activity;

public class ProposalActivityException extends Exception {

    public ProposalActivityException() {
        super();
    }

    public ProposalActivityException(String message, Throwable cause, boolean enableSuppression,
            boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    public ProposalActivityException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProposalActivityException(String message) {
        super(message);
    }

    public ProposalActivityException(Throwable cause) {
        super(cause);
    }

}
